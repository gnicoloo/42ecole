#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	void	*buffer;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		buffer = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = buffer;
	}
}
